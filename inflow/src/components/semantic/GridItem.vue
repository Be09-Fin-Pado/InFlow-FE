<template>
  <div :style="customStyle">
    <slot></slot>
  </div>
</template>

<script setup>
import { computed } from 'vue';

const props = defineProps({
  h: {
    type: String,
    default: 'auto',
  },
  w: {
    type: String,
    default: 'auto',
  },
  b: {
    type: String,
    default: 'none',
  },
  br: {
    type: String,
    default: '0rem',
  },
  bgc: {
    type: String,
    default: 'transparent',
  },
  fs: {
    type: String,
    default: '100%',
  },
  fw: {
    type: String,
    default: 'inherit',
  },
  c: {
    type: String,
    default: 'inherit',
  },
});

const customStyle = computed(() => ({
  display: 'grid',
  height: props.h,
  width: props.w,
  border: props.b,
  borderRadius: props.br,
  backgroundColor: props.bgc,
  fontSize: props.fs,
  fontWeight: props.fw,
  color: props.c
}));
</script>
